function sum(a, b) {
    const c = (a + b);
    return c;
}

function substract(a, b) {
    const d = (a - b);
    return d;
}

function multiply(a, b) {
    const e = (a * b);
    return e;
}

function divide(a, b) {
    const f = (a / b);
    return f;

}


export default { sum, substract, multiply, divide };